// for loop use when you know now many number of iterations has 5o be printed and set condition accordingly 
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// while loop used when you know condition but not number of iterations 
let j = 0;
while (j < 5) {
    console.log(j);
    j++;
}

// do-while loop used when you need output first then to check the condition 
let k = 0;
do {
    console.log(k);
    k++;
} while (k < 5);
